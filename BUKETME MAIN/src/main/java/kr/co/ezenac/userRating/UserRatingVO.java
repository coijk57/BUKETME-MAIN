package kr.co.ezenac.userRating;

public class UserRatingVO {

	private int profileregisternum;
	private int videonum;
	private int likes;
	private int bad;
	private int rating;
	
	public int getProfileregisternum() {
		return profileregisternum;
	}
	public void setProfileregisternum(int profileregisternum) {
		this.profileregisternum = profileregisternum;
	}
	public int getVideonum() {
		return videonum;
	}
	public void setVideonum(int videonum) {
		this.videonum = videonum;
	}
	public int getLikes() {
		return likes;
	}
	public void setLikes(int likes) {
		this.likes = likes;
	}
	public int getBad() {
		return bad;
	}
	public void setBad(int bad) {
		this.bad = bad;
	}
	public int getRating() {
		return rating;
	}
	public void setRating(int rating) {
		this.rating = rating;
	}
	
	
}
